public class Animal {

    private String animalType;
    private Integer animalLegs;
    private Boolean isVertebrate;

    public String getAnimalType() {
        return animalType;
    }

    public void setAnimalType(String animalType) {
        this.animalType = animalType;
    }

    public Integer getAnimalLegs() {
        return animalLegs;
    }

    public void setAnimalLegs(Integer animalLegs) {
        this.animalLegs = animalLegs;
    }

    public Boolean isVertebrate() {
        return isVertebrate;
    }

    public void setAnimalComparison(Boolean animalComparison) {
        this.isVertebrate = animalComparison;
    }




}
